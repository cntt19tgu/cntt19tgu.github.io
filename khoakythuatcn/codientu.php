                  </p>
                  <p>Ngoài ra, sinh viên được học lý thuyết kết hợp với thực hành trong phòng thí nghiệm chuẩn quốc tế với trang thiết bị hiện đại, tân tiến.</p>
                  <h5 class="w3-opacity" style="color: darkcyan; font-size: 20px;"><b>4. Học ngành Công nghệ kỹ thuật cơ điện tử ra trường làm gì?</b></h5>
                  <p>Sinh viên tốt nghiệp chương trình cử nhân Công nghệ kỹ thuật cơ điện tử có nhiều cơ hội việc làm hấp dẫn như:</p>
                  <p>Kỹ thuật viên thiết kế, lập trình, vận hành, bảo trì các thiết bị phần cứng, phần mềm tại các trung tâm nghiên cứu, doanh nghiệp có trang bị các hệ thống máy móc, dây chuyền tự động;</p>
                  <p>Chuyên viên tư vấn về công nghệ, thiết kế, lập trình, thi công và chuyển giao các dây chuyền, hệ thống tự động;</p>
