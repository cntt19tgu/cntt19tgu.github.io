                  <p>Mỗi ngành như cơ khí, điện tử, tin học đều có nền tảng khoa học vững chắc và tạo ra các sản phẩm đặc trưng riêng. Tuy nhiên, yêu cầu của thời đại đặt ra yêu cầu cao hơn về cách hoạt động của máy móc, yêu cầu máy móc cần phải gọn nhẹ hơn, linh động hơn, uyển chuyển hơn và thông minh hơn.  Các kỹ sư cơ khí không thể làm máy móc thông minh hơn, trong khi những kỹ sư tin học có thể tạo ra trí thông minh nhân tạo nhưng họ không biết về cơ khí, những kỹ sư điện tử có thể kết nối và điều khiển tín hiệu, nhưng họ không thể kết nối giữa trí thông minh nhân tạo để điều khiển thiết bị cơ khí. Chính yêu cầu này đã hình thành nên ngành Cơ điện tử để tạo ra sản phẩm mới đáp ứng các yêu cầu đặt ra trên cơ sở phối hợp nền tảng sẵn có của các ngành với nhau.</p>
