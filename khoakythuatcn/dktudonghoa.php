                  <h5 class="w3-opacity" style="color: darkcyan; font-size: 20px;"><b>3. Sinh viên tốt nghiệp từ chương trình Kỹ sư Điện tử - Tin học công nghiệp đạt được kiến thức:</b></h5>
                  <p>ngành Kỹ thuật điều khiển và Tự động hóa trang bị cho sinh viên kiến thức về: lý thuyết mạch điện - điện tử, kỹ thuật đo lường và các hệ thống cảm biến thông minh, các hệ thống mạng truyền thông công nghiệp, phương pháp điều khiển truyền thống và hiện đại, lập trình tự động hóa các dây chuyền sản xuất công nghiệp, điều khiển điện tử công suất và truyền động điện... Học ngành Công nghệ Kỹ thuật điều khiển và Tự động hóa sinh viên được đào tạo từ cơ bản đến chuyên sâu về thiết kế, xây dựng, vận hành, bảo trì các hệ thống điều khiển và tự động hóa, hệ thống truyền động điện, hệ thống truyền thông công nghiệp, hệ thống đo lường thông minh…</p>
