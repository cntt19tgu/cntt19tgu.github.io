                  <h5 class="w3-opacity" style="color: darkcyan; font-size: 20px;"><b>2. Những mục tiêu của ngành</b></h5>
                  <p>Chương trình đào tạo Công nghệ kỹ thuật Điện tử và Tin học công nghiệp hướng đến đào tạo nguồn nhân lực chất lượng cao trên cơ sở kết hợp các lĩnh vực: Điện – Điện tử, Công nghệ thông tin, Hệ thống nhúng, Internet vạn vật (IoT), Tự động hóa công nghiệp. Năm 2020, Trường Đại học Tiền Giang tuyển sinh 40 chỉ tiêu ngành Công nghệ kỹ thuật Điện tử và Tin học công nghiệp.</p>
                  <p>Đào tạo kỹ sư ngành Công nghệ kỹ thuật Điện tử – Tin học công nghiệp có kiến thức khoa học cơ bản; có năng lực chuyên môn vững vàng; có khả năng phân tích, giải quyết độc lập các vấn đề trong lĩnh vực Điện - Điện tử - Ứng dụng tin học trong công nghiệp; có khả năng tham gia vào các hoạt động xã hội với phẩm chất đạo đức nghề nghiệp; có khả năng tự nghiên cứu nâng cao trình độ chuyên môn, nghiệp vụ để phù hợp với môi trường làm việc năng động và xu thế hội nhập cao, góp phần vào sự nghiệp công nghiệp hóa - hiện đại hóa đất nước..</p>
