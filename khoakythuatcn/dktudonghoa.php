                  <h5 class="w3-opacity" style="color: darkcyan; font-size: 20px;"><b>1. GIới thiệu:</b></h5>
                  <p>Kỹ thuật điều khiển và Tự động hóa (một số trường đại học là ngành Công nghệ Kỹ thuật điều khiển và Tự động hóa) là ngành học nghiên cứu, thiết kế, vận hành các hệ thống tự động, dây chuyền sản xuất tự động tại các nhà máy xi măng, sắt thép, nước giải khát và thiết kế, điều khiển, chế tạo robot, quản lý sản phẩm tại các công ty trong và ngoài. Ngành này luôn gắn liền với các quá trình sản xuất trong công nghiệp, nơi mà các thao tác của con người sẽ được thay thế hoàn toàn bằng các hoạt động của máy móc, robot tự động; giúp tăng năng suất lao động, giảm thiểu nhân công, nhân lực thời gian và chi phí.</p>
