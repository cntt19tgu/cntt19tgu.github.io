                  <p>+ Có khả năng thiết kế, lập trình và sử dụng các phần mềm ứng dụng, phần mềm điều khiển, phần mềm nhúng cho các thiết bị thông minh.</p>
                  <p>+ Có phương pháp làm việc khoa học, khả năng giải quyết vấn đề độc lập, khả năng hoạt động nhóm hiệu quả.</p>
                  <h5 class="w3-opacity" style="color: darkcyan; font-size: 20px;"><b>4. Sinh viên tốt nghiệp đại học ngành CNKT Điện tử - Tin học công nghiệp có thể đảm nhận công việc ở các vị trí sau:</b></h5>
                  <p>+ Kỹ sư trực tiếp hoặc quản lý tại các nhà máy sản xuất, lắp ráp thiết bị điện, điện tử; các cơ sở khai thác, sửa chữa, kinh doanh về lĩnh vực điện, điện tử.</p>
                  <p>+ Cán bộ, quản lý điều hành kỹ thuật, tại các doanh nghiệp thiết kế, chế tạo, sản xuất trong lĩnh vực tự động hóa. </p>
                  <p>+ Lập trình viên, kỹ thuật viên tại các công ty thiết kế và sản xuất các phần mềm chuyên dụng, các phần mềm nhúng cho các thiết bị thông minh.</p>
